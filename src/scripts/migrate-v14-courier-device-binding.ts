import fs from 'fs';
import path from 'path';
import { createClient } from '@libsql/client';

function loadEnvLocal() {
  const envPath = path.join(process.cwd(), '.env.local');
  if (!fs.existsSync(envPath)) return;
  for (const line of fs.readFileSync(envPath, 'utf8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const index = trimmed.indexOf('=');
    if (index > 0) process.env[trimmed.slice(0, index).trim()] = trimmed.slice(index + 1).trim();
  }
}

async function main() {
  loadEnvLocal();
  const url = process.env.TURSO_DATABASE_URL?.replace(/^libsql:\/\//, 'https://');
  const authToken = process.env.TURSO_AUTH_TOKEN;
  if (!url || !authToken) throw new Error('TURSO_DATABASE_URL dan TURSO_AUTH_TOKEN wajib ada.');
  const client = createClient({ url, authToken });

  const statements = [
    `ALTER TABLE couriers ADD COLUMN device_id TEXT`,
    `ALTER TABLE courier_sessions ADD COLUMN device_id TEXT`,
    `ALTER TABLE courier_sessions ADD COLUMN is_active INTEGER NOT NULL DEFAULT 1`,
  ];

  for (const statement of statements) await client.execute(statement);
  console.log('migrate-v14 (courier device binding) completed');
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
